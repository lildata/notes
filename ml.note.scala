Object MachineLearning {

	val terminology = Map(
"Observations" -> "Iterms or entities used for learning or evaluation, e.g. emails for spam classification",
"Features" -> "Attributes (typically numeric) used to represent an observation, e.g., length, data, etc."
"Labels" -> "Values / categories assigned to objervations, e.g., spam / not-spam",
"Training data" -> "Observations used to train a learning algorithm, e.g., a set of emails along with their labels.",
"Test data" -> "Observations used to evaluate a learning algorithm",
"Supervied learning" -> "Learning from labeled observations (labels 'teach' the algorithm to learn mapping from observation to labels)",
"Unsupervied learning" -> "Learning from unlabeled observations (the algorithm must find latent structure from features alone).\nCan be a goal in itself like discover hidden patterns or exploratory data analysis"
	)
	


}
